                    WRITE (*, *) "" 
                END IF   
            END IF   
        END IF   
          
    END IF   
    IF (dtype_check_status%numTotal == dtype_check_status%numIdentical) THEN 
        check_status%numIdentical = check_status%numIdentical + 1 
    ELSE IF (dtype_check_status%numOutTol > 0) THEN 
